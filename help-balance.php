      <article dir="ltr">
        <p class="no-margin">Сначала вам необходимо усвоить основную терминологию балансового отчета. Балансовый отчет представляет активы компании, ее пассивы – обязательства компании перед третьими лицами – и накопленные инвестиции владельцев компании. Балансовый отчет отражает баланс на конкретную дату. Это – моментальная фотография, показывающая все, что принадлежит компании в конкретный момент времени. Балансовый отчет – это основа всех учетных записей, и вы обязаны его знать. Рассмотрим компоненты балансового отчета. </p>
        <p><b>Активы</b> – это ресурсы, которыми располагает компания для успешного ведения своей хозяйственной деятельности. Сюда относятся:</p>
          <li>Внеоборотные активы;</li>
          <li>Оборотные активы;</li>
          <li>Дебиторская задолженность</li>
        <p><b>Пассивы</b> – это представленные в денежном выражении обязательства по возврату ссуд, долгов и иных заемных средств на производство товаров или оказание услуг:</p>
          <li>Кредиторская задолженность.</li>
        <p><b>Внеоборотные активы</b> — это имущество, которое используется в производственном процессе более одного года и его стоимость включается в себестоимость готовой продукции по частям. К внеоборотным активам относят основные средства, в состав которых входят здания, производственное оборудование, транспорт и прочее:</p>
          <li>Основные средства.</li>
        <p><b>Оборотные активы</b> — это имущество, которое сразу идет в оборот (то есть непосредственно используется в производственном процессе) и должно принести доход в течение года. Стоимость оборотных активов включается в себестоимость готовой продукции сразу:</p>
          <li>Запасы.</li>
        <p><b>Дебиторская задолженность</b> — суммы денег, которые предприятию должны другие компании, государство или физические лица. Тот, кто имеет долг перед предприятием, называется дебитором. Например, компания продала товар другой фирме, но деньги за продажу не получила:</p>
          <li>Неоплаченные заказы;</li>
          <li>Задолженость по зарплате;</li>
          <li>Прочая дебиторская задолженость;</li>
        <p><b>Кредиторская задолженность</b> — задолженность субъекта (предприятия, организации, физического лица) перед другими лицами, которую этот субъект обязан погасить. Кредиторская задолженность возникает в случае, если дата поступления услуг (работ, товаров, материалов и т. д.) не совпадает с датой их фактической оплаты:</p>
          <li>Предоплаты по заказам;</li>
          <li>Задолженость по зарплате;</li>
          <li>Прочая кредиторская задолженость</li>
        <p><b>Чистые активы</b> — это реальная стоимость имущества, имеющегося у организации, ежегодно определяемая за вычетом его долгов, то есть разница между активами и обязательствами компании.</p>
